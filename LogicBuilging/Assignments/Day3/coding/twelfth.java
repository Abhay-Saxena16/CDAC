/* Q 12.12. Write a program to draw the following pattern: 
		
			***** 
			***** 
			***** 
			***** 
			*****   */

class twelfth{
    public static void main(String[] args) {
       
	   int i =0 ,j = 0;
	   
	   for(i=0;i<=4;i++){
			for(j=0;j<=4;j++){
			System.out.print("*");
			}
			System.out.println();
		}
	   
    }
}
